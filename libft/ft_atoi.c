#include "libft.h"

int		ft_atoi(const char *str)
{
	int	i;
	int	x;
	int z;

	x = 0;
	z = 1;
	i = -1;
	if (str[0] == '-')
		z = ++i - 1;
	while (*str == '\n' || *str == '\t' || *str == ' ')
		str++;
	while (str[++i] && str[i] >= '0' && str[i] <= '9')
		x = x * 10 + (str[i] - 48);
	return (x * z);
}
