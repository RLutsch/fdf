#include "libft.h"

int	ft_strncmp(char const *s1, char const *s2, size_t n)
{
	size_t			i;
	unsigned char	*tmp1;
	unsigned char	*tmp2;

	tmp1 = (unsigned char *)s1;
	tmp2 = (unsigned char *)s2;
	i = 0;
	while ((*tmp1++ || *tmp2++) && n--)
	{
		if (tmp1 != tmp2)
			return (tmp1 - tmp2);
	}
	return (0);
}
